def pad_data(orig_list): # pads any matrix that isnt fully enclosed in blocks (prevent player escaping the map)
    List = orig_list
    top = List[0]  # takes the first list in the matrix
    bottom = List[-1]  # takes the final list in the matrix
    left = []
    right = []
    ideal_list = [1]*len(List)  # creates data to pad the top/bottom with if needed
    for row in List:
        left.append(row[0])   # adds the first element in each list to Left / right
        right.append(row[-1])
    if (0 in top) or (2 in top):
        List.insert(0, ideal_list)  # pads top
    if (0 in bottom) or (2 in bottom):
        List.append(ideal_list)  # pads bottom
    if (0 in left) or (2 in left):
        for item in List:
            item.insert(0, 1)  # pads left
    if (0 in right) or (2 in right):
        for item in List:
            item.append(1)  # pads right
    length = 1
    changed = True
    while changed:    # normalises row length (required if both veritical and hirizontal padding have been done)
        changed = False
        for item in List:
            if len(item)>length:  # gets longest item in list and saves length value
                length = len(item)
                changed=True
            while length > len(item):   # if item is less than what it should be, pads it out
                item.append(1)
                changed = True
    return List

def trim_data(orig_list):  # removes excess padding from map creation (for performance)
    List = orig_list
    changed = True
    while changed:
        changed = False
        if (0 not in List[0]) and (0 not in List[1]): # Top
            changed = True
            del List[0]
        if (0 not in List[-1]) and (0 not in List[-2]): # Bottom
            changed = True
            del List[-1]
        first = []
        second = []
        last = []
        penultimate = []
        for item in List:  # creates a list of the first 2 colunms and the last 2 columns
            first.append(item[0])
            second.append(item[1])  # creates lists from first 2 columns

            last.append(item[-1])   # creates lists from last 2 columns
            penultimate.append(item[-2])
        if (0 not in first) and (0 not in second):  # Left
            for item in List:
                del item[0]
            changed = True
        if (0 not in last) and (0 not in penultimate):   # Right
            for item in List:
                del item[-1]
            changed = True
    return List